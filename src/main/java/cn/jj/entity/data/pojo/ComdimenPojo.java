package cn.jj.entity.data.pojo;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

/**
 * 
* Title: ComdimenPojo 目的地 景点 评分实体
* Description
* @author 赵乐
* @Date 2017年11月7日 上午11:28:15
 */
@JsonIgnoreProperties(ignoreUnknown = true)//vo中缺少json的某个字段属性
public class ComdimenPojo {
	
	private String avgScore;//平均分
	private String commentCount;//总条数
	private String formatAvgScore;//百分比
	
	public String getAvgScore() {
		return avgScore;
	}
	public void setAvgScore(String avgScore) {
		this.avgScore = avgScore;
	}
	public String getCommentCount() {
		return commentCount;
	}
	public void setCommentCount(String commentCount) {
		this.commentCount = commentCount;
	}
	public String getFormatAvgScore() {
		return formatAvgScore;
	}
	public void setFormatAvgScore(String formatAvgScore) {
		this.formatAvgScore = formatAvgScore;
	}
	
	@Override
	public String toString() {
		return "ComdimenPojo [avgScore=" + avgScore + ", commentCount=" + commentCount + ", formatAvgScore="
				+ formatAvgScore + "]";
	}
	
	
	
}
