
package cn.jj.entity.data.pojo;

import java.io.Serializable;

import cn.jj.entity.data.Addressinfo;
import cn.jj.entity.data.Hotelinfo;
import cn.jj.entity.data.Url;

/**
 * 存放url、basicInfo、addressInfo、pictureInfo组装对象
 * @author 汤玉林
 * 2017年11月9日
 */
public class HotelBasicInfoPojo implements Serializable{

	private static final long serialVersionUID = -909267629470367370L;

	private Url url;
	
	private Hotelinfo basicInfo;
	
	private Addressinfo addressInfo;

	public Url getUrl() {
		return url;
	}

	public void setUrl(Url url) {
		this.url = url;
	}

	public Hotelinfo getBasicInfo() {
		return basicInfo;
	}

	public void setBasicInfo(Hotelinfo basicInfo) {
		this.basicInfo = basicInfo;
	}

	public Addressinfo getAddressInfo() {
		return addressInfo;
	}

	public void setAddressInfo(Addressinfo addressInfo) {
		this.addressInfo = addressInfo;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	


	
}
